int	ft_isnum(const char *num)
{
	int	i;

	i = -1;
	if (num[0] == '-' || num[0] == '+')
		++i;
	while (num[++i])
	{
		if (!('0' <= num[i] && num[i] <= '9'))
			return (0);
	}
	return (1);
}
