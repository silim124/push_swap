#include "libft.h"

int	ft_isint(char *num)
{
	if (num[0] == '+' || ('0' <= num[0] && num[0] <= '9'))
	{
		if (ft_atoi(num) < 0)
			return (0);
	}
	else if (num[0] == '-')
	{
		if (ft_atoi(num) >= 0)
			return (0);
	}
	return (1);
}
